export function getCarById(id: string): Car | undefined {
  return cars.find(car => car.id === id)
}

export function searchCars(filters: {
  condition?: string
  brand?: string
  model?: string
  type?: string
  minPrice?: number
  maxPrice?: number
  fuel?: string
  transmission?: string
}): Car[] {
  return cars.filter(car => {
    if (filters.condition && filters.condition !== 'all') {
      if (filters.condition === 'used' && car.condition !== 'Used') return false
      if (filters.condition === 'new' && car.condition !== 'New') return false
    }
    if (filters.brand && filters.brand !== 'All' && car.brand !== filters.brand) return false
    if (filters.type && filters.type !== 'All' && car.type !== filters.type) return false
    if (filters.minPrice && car.price < filters.minPrice) return false
    if (filters.maxPrice && car.price > filters.maxPrice) return false
    if (filters.fuel && filters.fuel !== 'All' && car.fuel !== filters.fuel) return false
    if (filters.transmission && filters.transmission !== 'All' && car.transmission !== filters.transmission) return false
    return true
  })
}
